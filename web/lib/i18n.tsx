const LANG_KEY = "loadify_lang";

interface I18n {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string) => string;
}

const I18nContext = createContext<I18n>({
  lang: "zh",
  setLang: () => {},
  t: (k) => k,
});

export function LocaleProvider({ children }: { children: React.ReactNode }) {
  // Default to Chinese; both server and first client render use "zh" so there
  // is no hydration mismatch, then the stored preference is applied on mount.
  const [lang, setLangState] = useState<Lang>("zh");

  useEffect(() => {
    const stored = window.localStorage.getItem(LANG_KEY) as Lang | null;
    if (stored === "zh" || stored === "en") setLangState(stored);
  }, []);

  const setLang = (l: Lang) => {
    setLangState(l);
    window.localStorage.setItem(LANG_KEY, l);
    document.documentElement.lang = l === "zh" ? "zh-CN" : "en";
  };

  const t = (key: string) => messages[lang][key] ?? messages.en[key] ?? key;

  return <I18nContext.Provider value={{ lang, setLang, t }}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}

// statusLabel localizes a run/worker status via the status.* keys, falling back
// to the raw value for any status without a translation, so an unexpected
// status never renders as a leaked i18n key.
export function statusLabel(t: (k: string) => string, status: string): string {
  const key = `status.${status}`;
  const out = t(key);
  return out === key ? status : out;
}

// roleLabel / protocolLabel localize their enum, falling back to the raw value.
export function roleLabel(t: (k: string) => string, role: string): string {
  const key = `role.${role}`;
  const out = t(key);
  return out === key ? role : out;
}
export function protocolLabel(t: (k: string) => string, p: string): string {
  const key = `protocol.${p}`;
  const out = t(key);
  return out === key ? p : out;
}
